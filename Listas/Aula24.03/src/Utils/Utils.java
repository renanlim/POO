package Utils;

public class Utils {
	public static double getMedia2(double a, double b) {
		return (a+b)/2;
	}
	public static double getMedia3(double a, double b, double c) {
		return (a+b+c)/3;
	}
	public static double getMedia4(double a, double b, double c, double d) {
		return (a+b+c+d)/4;
	}
}
